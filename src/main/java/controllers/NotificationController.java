package controllers;

import com.google.inject.Inject;
import com.google.inject.Singleton;
import ninja.Context;
import ninja.Result;
import ninja.Results;
import ninja.params.Param;
import services.BacklogService;
import services.NotificationService;


@Singleton
public class NotificationController {
    @Inject
    private NotificationService notificationService;
    @Inject
    private BacklogService backlogService;

    public Result sendNotifications(@Param("length") Integer length) {

        notificationService.sendPaymentNotification(length);
        return Results.ok().json();
    }

    public Result processPaymentBacklog(@Param("length") Integer length, Context context) {
        backlogService.processAllPaymentNotifications(context);
        return Results.ok().json();
    }
}
